function hesapla() {
    var urunAdi = hesaplama.urunAdi.value;
    var listeFiyat = hesaplama.listeFiyat.value;
    var satisFiyat = hesaplama.satisFiyat.value;
    var agirlik = hesaplama.agirlik.value;
    var tahsilat = hesaplama.tahsilat.value;
    if (listeFiyat == "" || satisFiyat == "" || agirlik == "" || tahsilat == "") {
        alert("Lütfen bütün alanları doldurunuz.");
        return;
    }

    if (isNaN(listeFiyat) || isNaN(satisFiyat || isNaN(agirlik))) {
        alert("Lütfen numarasal değerler giriniz. (Ondalık sayılar nokta ile ayrılıyor)");
        return;
    }

    if (tahsilat == "bir") {
        var yuzde = 1;
    } else if (tahsilat == "iki") {
        var yuzde = 0.9;
    } else if (tahsilat == "uc") {
        var yuzde = 0.8;
    } else if (tahsilat == "dort") {
        var yuzde = 0.5;
    } else if (tahsilat == "bes") {
        var yuzde = 0.2;
    } else if (tahsilat == "alti") {
        var yuzde = 0.15;
    }
    var result = ((satisFiyat - listeFiyat) * agirlik * yuzde) / 2;
    hesaplama.sonuc.value = result;
}

